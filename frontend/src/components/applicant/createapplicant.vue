<script>
import applicantDataService from "../../Services/applicantDataService";
export default {
    name:'applicant-new',
    data () {
    return {
      applicant: {
        id: null,
        first_name: '',
        last_name: '',
        email: '',
        contact: '',
        password:'',
		message:''
      }
    }
  },
  
   methods: {


	   checkForm:function(e) {
      this.errors = [];
      if(!this.first_name) this.errors.push("firstName required.");
	  if(!this.last_name) this.errors.push("lastName required.");
	  if(!this.contact) this.errors.push("contact is  required.");
	  if(!this.password ) this.errors.push("password  is required.");





      if(!this.email) {
        this.errors.push("Email required.");
      } else if(!this.validEmail(this.email)) {
        this.errors.push("Valid email required.");        
      }
      if(!this.errors.length) return true;
      e.preventDefault();
    },
    validEmail:function(email) {
      var re = /^(([^<>()\[\]\\.,;:\s@"]+(\.[^<>()\[\]\\.,;:\s@"]+)*)|(".+"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/;
    return re.test(email);
    }
   },

    saveApplicant() {
      var data = {
        first_name:this.applicant.first_name,
		last_name:this.applicant.last_name,
		email:this.applicant.email,
		contact:this.applicant.contact,
		password:this.applicant.password
      };
      applicantDataService.create(data)
        .then(response => {
          this.applicant.id = response.data.id;
          console.log(response.data);
          this.submitted = true;
        })
        .catch(e => {
          console.log(e);
        });
	},
    newapplicant() {
      this.submitted = false;
      this.applicant = {};
    }
  }


</script>
